let status = 'lista';
switch (status){
    case 'aprovada':
        console.log("parabens");
        break;
    case 'lista':
        console.log("lista de espera");
        break;
    case 'reprovada':
        console.log("reprovada, infelizmente");
        break;

    default:
        console.log("informação incorreta");
}

